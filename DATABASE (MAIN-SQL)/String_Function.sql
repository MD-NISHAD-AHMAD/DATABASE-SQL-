CREATE TABLE T1 AS
SELECT 
      'HTTPS://WWW.GOOGLE.COM' AS
      CURRENT_STRING
      UNION ALL
      SELECT 
           'WWW\.GOOGLE.COM' AS CURRENT_STRING
           UNION ALL
           SELECT 
                 'WWW.GOOGLE.CO.IN' AS CURRENT_STRING;
                 
SELECT * FROM T1;

SELECT 
     CURRENT_STRING,
     SUBSTRING_INDEX(CURRENT_STRING, '.', 2),
     
     SUBSTRING_INDEX(SUBSTRING_INDEX(CURRENT_STRING, '.', 2), '.', -1)
     FROM
     T1;
     
     -- TRIMMING FUNCTION (LTRIM, RTRIM, TRIM)
     
     
     SELECT 
	ORIGINAL_STRING, LENGTH(ORIGINAL_STRING)
    AS LENGTH_OF_ORIGINAL_STRING, 
	LTRIM(ORIGINAL_STRING) 
    AS LEFT_TRIMMED_STRING,  LENGTH(LTRIM(ORIGINAL_STRING))
    AS LENGTH_OF_LEFT_TRIMMED_STRING, 
    RTRIM(ORIGINAL_STRING) 
    AS RIGHT_TRIMMED_STRING, LENGTH(RTRIM(ORIGINAL_STRING)) 
    AS LENGTH_OF_RIGHT_TRIMMED_STRING, 
    TRIM(ORIGINAL_STRING)  
    AS BOTH_SIDE_TRIMMED_STRING, LENGTH(TRIM(ORIGINAL_STRING)) AS LENGTH_OF_BOTH_SIDE_TRIMMED_STRING
FROM 
(
SELECT 
	'    ABCD    ' AS ORIGINAL_STRING
) AS T; 


CREATE TABLE TRIMMING AS 
SELECT 'ORIGINAL STRING' AS STRING_TYPE, '    ABCD    ' AS ORIGINAL_STRING 
UNION ALL 
SELECT 'LEFT TRIMMED STRING' AS STRING_TYPE, LTRIM('    ABCD    ') AS ORIGINAL_STRING 
UNION ALL 
SELECT 'RIGHT TRIMMED STRING' AS STRING_TYPE, RTRIM('    ABCD    ') AS ORIGINAL_STRING 
UNION ALL 
SELECT 'BOTH SIDE TRIMMED STRING' AS STRING_TYPE, TRIM('    ABCD    ') AS ORIGINAL_STRING; 

SELECT * FROM TRIMMING;

SELECT STRING_TYPE, 
ORIGINAL_STRING, 
LENGTH(ORIGINAL_STRING) 
AS STRING_LENGTH
FROM TRIMMING; 

