CREATE TABLE QUOTE AS
SELECT "CHANGE" AS PART_1, "IS" AS
PART_2, "IMPOSSIBLE" AS PART_3,
"IN" AS PART_4, "THIS" AS PART_5,
"FOG" AS PART_6, "OF" AS PART_7,
"IGNORANCE" AS PART_8;

SELECT * FROM QUOTE;

SELECT *, CONCAT(PART_1, " ", PART_2, " ", PART_3, " ", PART_4, " ", 
PART_5, " ", PART_6, " ", PART_7, " ", PART_8) AS FINAL_QUOTE
FROM QUOTE;

SELECT *, CONCAT_WS(" ", PART_1, PART_2, PART_3, PART_4, PART_5,
PART_6, PART_7, PART_8) AS FINAL_QUOTE
FROM QUOTE;

