USE db;

-- DATE / TIME FUNCTIONS

-- 1. CURRENT DATE / TIME FUNCTIONS.

SELECT 
       CURRENT_DATE() AS CURRENT_DATE_FUNCTION_1,
       CURRENT_DATE() AS CURRENT_DATE_FUNCTION_2;
       
SELECT 
       CURRENT_TIME() AS CURRENT_TIME_FUNCTIONS_1,
       CURTIME() AS CURRENT_TIME_FUNCTION_2;
       
SELECT 
      NOW() AS TIME_STAMP_FORMAT;
      
-- DATE TIME EXTRACTION FUNCTIONS.

SELECT 
    CD,
    DATE(CD)        AS CURRENT_DATE_DETAILS,
    DAY(CD)         AS CURRENT_DAY_DETAILS,
    MONTH(CD)       AS CURRENT_MONTH_DETAILS,
    YEAR(CD)        AS CURRENT_YEAR_DETAILS,
    DAYNAME(CD)     AS TODAYS_DAY,
    DAYOFMONTH(CD)  AS DAY_OF_MONTH,
    DAYOFWEEK(CD)   AS DAY_OF_WEEK,
    WEEKDAY(CD)     AS WEEK_DAY,
    DAYOFYEAR(CD)   AS DAY_OF_YEAR,
    HOUR(CD)        AS CURRENT_HOUR_DETAILS,
    MINUTE(CD)      AS CURRENT_MINUTE_DETAILS,
    SECOND(CD)      AS CURRENT_SECOND_DETAILS,
    MONTHNAME(CD)   AS CURRENT_MONTH_NAME,
    QUARTER(CD)     AS CURRENT_QUARTER,
    WEEK(CD)        AS CURRENT_WEEK
FROM
(
    SELECT NOW() AS CD
) AS T;

-- Date / Time Formatting Functions.

SELECT NOW() AS CURRENT_DETAILS;

SELECT DATE_FORMAT(NOW(), '%d %b %Y');

SELECT DATE_FORMAT(NOW(), '%D %M %y');

SELECT DATE_FORMAT(NOW(), '%d(%a) %b %Y');

SELECT DATE_FORMAT(NOW(), '%d(%a) %b %Y %H:%I:%S %P') AS CUSTOMIZED_FORMAT;

SELECT TIME_FORMAT(CURRENT_TIME(), '%S:%i:%H %p');

-- DATE / TIME DIFFERENCE FUNCTIONS.

SELECT DATEDIFF(CURRENT_DATE(), '2026-01-01');

SELECT TIMEDIFF(CURRENT-TIME(), '00:00:01');

SELECT  PERIOD_DIFF('202603', '202601'); 

-- DATE / TIME MODIFICATION FUNCTIONS.

SELECT  
       CURRENT_DATE() AS
       PROJECT_START_DATE,
          ADDDATE(CURRENT_DATE(),
	INTERVAL 2 MONTH) AS PROJECT_END_DATE,
      SUBDATE(CURRENT_DATE(),
      INTERVAL 10 DAY) AS FIRST_MEETING;

SELECT 
       CURRENT_TIME() AS CURRENT_TIME_, ADDTIME(CURRENT_TIME(), '5:00:00'),
       SUBTIME(CURRENT_TIME(), '6:00:00');
       
