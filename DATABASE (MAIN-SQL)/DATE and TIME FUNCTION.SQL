USE db;

-- DATE / TIME FUNCTIONS

-- 1. CURRENT DATE / TIME FUNCTIONS.

SELECT 
       CURRENT_DATE() AS CURRENT_DATE_FUNCTION_1,
       CURRENT_DATE() AS CURRENT_DATE_FUNCTION_2;
       
SELECT 
       CURRENT_TIME() AS CURRENT_TIME_FUNCTIONS_1,
       CURTIME() AS CURRENT_TIME_FUNCTION_2;
       
SELECT 
      NOW() AS TIME_STAMP_FORMAT;
      
-- DATE TIME EXTRACTION FUNCTIONS.

SELECT 
    CD,
    DATE(CD)        AS CURRENT_DATE_DETAILS,
    DAY(CD)         AS CURRENT_DAY_DETAILS,
    MONTH(CD)       AS CURRENT_MONTH_DETAILS,
    YEAR(CD)        AS CURRENT_YEAR_DETAILS,
    DAYNAME(CD)     AS TODAYS_DAY,
    DAYOFMONTH(CD)  AS DAY_OF_MONTH,
    DAYOFWEEK(CD)   AS DAY_OF_WEEK,
    WEEKDAY(CD)     AS WEEK_DAY,
    DAYOFYEAR(CD)   AS DAY_OF_YEAR,
    HOUR(CD)        AS CURRENT_HOUR_DETAILS,
    MINUTE(CD)      AS CURRENT_MINUTE_DETAILS,
    SECOND(CD)      AS CURRENT_SECOND_DETAILS,
    MONTHNAME(CD)   AS CURRENT_MONTH_NAME,
    QUARTER(CD)     AS CURRENT_QUARTER,
    WEEK(CD)        AS CURRENT_WEEK
FROM
(
    SELECT NOW() AS CD
) AS T;